#include "lib_for_pipex.h"

int	ft_arr_count(char **array)
{
	int	i;

	if (!array || !array[0])
	{
		return (0);
	}
	i = 0;
	while (array[i] != NULL)
	{
		i++;
	}
	return (i);
}
